    private void btLocalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLocalizarActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Escolha o arquivo no formato txt com o automato!");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.txt", "*txt","txt");
        fileChooser.setFileFilter(filter);
        String caminho_padrao = System.getProperty("user.dir");
        File pathInicial = new File(caminho_padrao);
        fileChooser.setCurrentDirectory(pathInicial);
        int retorno = fileChooser.showOpenDialog(fileChooser);
        if(retorno == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            caminhoArquivo.setText(file.getPath());
        }
    }//GEN-LAST:event_btLocalizarActionPerformed

