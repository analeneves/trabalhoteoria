    public void testaPalavra(){
        if(radioAFN.isSelected()) {
            AFN afn = new AFN();
            if(afn.Verifica(editTextPalavra.getText(),caminhoArquivo.getText())){
                labelResultado.setText("Palavra Aceita!");
            } else {
                labelResultado.setText("Palavra nao Aceita!");
            }
        } else {
            AFD afd = new AFD();
            if(afd.Verifica(editTextPalavra.getText(),caminhoArquivo.getText())){
                labelResultado.setText("Palavra Aceita!");
            } else {
                labelResultado.setText("Palavra nao Aceita!");
            }
        }
    }
